    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        int salesid= Integer.parseInt(jLabel7.getText());
        String date = jLabel4.getText();
        String amount = jLabel6.getText();
        String status ="OK";
        try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/project","root","Shivam@123");
            PreparedStatement p = con.prepareStatement("insert into orders (salesid,date,amount,status) values (?,?,?,?)");
            p.setInt(1, salesid);
            p.setString(2, date);
            p.setString(3, amount);
            p.setString(4, status);
            int rows = p.executeUpdate();
            if (rows > 0) {
                JOptionPane.showMessageDialog(rootPane,"Bill Created");
            }
            else{
                JOptionPane.showMessageDialog(rootPane,"Bill Creation Failed");
            }
        }
        catch(Exception e){
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton3ActionPerformed

