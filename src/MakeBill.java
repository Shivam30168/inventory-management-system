    private void displayData() {
    jLabel7.setText(salesId);
    SimpleDateFormat dateFormat = new SimpleDateFormat("dd MMM yyyy");
    Date pd;
    try {
        pd = new SimpleDateFormat("EEE, MMM d, yyyy hh:mm:ss a").parse(billDate);
        jLabel4.setText(dateFormat.format(pd));
    } catch (Exception e) {
        jLabel4.setText(billDate);
    }
    jTable1.setModel(tableModel);
    jLabel6.setText(String.valueOf(totalAmount));
}
