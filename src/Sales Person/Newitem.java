    private void initializeTable() {
        String[] columnNames = {"Item_Id", "Item_Name", "Item_Category", "Item_Units", "No_of_Units", "Item_Price"};

        tableModel = new DefaultTableModel(columnNames,0);

        jTable1.setModel(tableModel);
    }

    private void fetchDataFromDatabase() {
        
        try{
                    Class.forName("com.mysql.jdbc.Driver");
                    Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/project","root","Shivam@123");
                    PreparedStatement p = con.prepareStatement("Select * from Newitem ");
                    ResultSet rs = p.executeQuery();
            
            while (rs.next()) {
                Object[] row = {
                        rs.getString("Item_Id"),
                        rs.getString("Item_Name"),
                        rs.getString("Item_Category"),
                        rs.getString("Item_Units"),
                        rs.getInt("No_of_Units"),
                        rs.getInt("Item_Price")
                };
                tableModel.addRow(row);
            }
}
         catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e);
        }
    }
    
