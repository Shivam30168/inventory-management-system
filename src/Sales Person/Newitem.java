    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        String id=jTextField1.getText();
        String name = jTextField2.getText();
        String category = (String)jComboBox1.getSelectedItem();
        String unit = (String)jComboBox2.getSelectedItem();
        int units = Integer.parseInt(jTextField3.getText());
        int price = Integer.parseInt(jTextField4.getText());
        try{
                    Class.forName("com.mysql.jdbc.Driver");
                    Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/project","root","Shivam@123");
                    PreparedStatement p = con.prepareStatement(" INSERT INTO Newitem (Item_Id, Item_Name, Item_Category, Item_Units, No_of_Units, Item_Price) VALUES (?, ?, ?, ?, ?, ?)");
                    p.setString(1, id);
                    p.setString(2, name);
                    p.setString(3, category);
                    p.setString(4, unit);
                    p.setInt(5, units);
                    p.setInt(6,price);                
                    int rows = p.executeUpdate();
                    if (rows > 0) {
                        JOptionPane.showMessageDialog(rootPane, "Successfully Inserted");
                        tableModel.setRowCount(0);
                        fetchDataFromDatabase();
                    } else {
                        JOptionPane.showMessageDialog(rootPane, "Failed to insert data");
                    }
                    }
         catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e);
        }
        
    }//GEN-LAST:event_jButton4ActionPerformed
